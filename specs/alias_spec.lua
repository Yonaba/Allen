	end)
	
end)

context('Enabling aliases', function()
	package.loaded.allen = nil
	_G.ALLEN_ALIASES = true
	local _ = require 'allen'
	
	test('Setting ALLEN_ALIASES to true before calling Allen will import aliases', function()
		assert_not_nil(_.capFirst)
		assert_not_nil(_.capEach)
		assert_not_nil(_.caps)
		assert_not_nil(_.cap)
		assert_not_nil(_.isLower)
		assert_not_nil(_.isUpper)
		assert_not_nil(_.startsLower)
		assert_not_nil(_.startsUpper)
		assert_not_nil(_.split)
		assert_not_nil(_.trim)
		assert_not_nil(_.esc)
		assert_not_nil(_.subst)
		assert_not_nil(_.interpolate)
		assert_not_nil(_.isNum)
		assert_not_nil(_.charAt)
		assert_not_nil(_.next)
		assert_not_nil(_.numFmt)
		assert_not_nil(_.rjust)
		assert_not_nil(_.ljust)
		assert_not_nil(_.center)
		assert_not_nil(_.isLuaKword)
		assert_not_nil(_.isReserved)
		assert_not_nil(_.isOperator)
		assert_not_nil(_.isOp)
		assert_not_nil(_.isIden)
		assert_not_nil(_.isVarName)
		assert_not_nil(_.stats)
	end)
	
end)
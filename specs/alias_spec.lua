	local _ = require 'allen'
	test('Not defining ALLEN_ALIASES before calling Allen will not import aliases', function()
		assert_nil(_.capFirst)
		assert_nil(_.capEach)
		assert_nil(_.caps)
		assert_nil(_.cap)
		assert_nil(_.isLower)
		assert_nil(_.isUpper)
		assert_nil(_.startsLower)
		assert_nil(_.startsUpper)
		assert_nil(_.split)
		assert_nil(_.trim)
		assert_nil(_.esc)
		assert_nil(_.subst)
		assert_nil(_.interpolate)
		assert_nil(_.isNum)
		assert_nil(_.charAt)
		assert_nil(_.next)
		assert_nil(_.numFmt)
		assert_nil(_.rjust)
		assert_nil(_.ljust)
		assert_nil(_.center)
		assert_nil(_.isLuaKword)
		assert_nil(_.isReserved)
		assert_nil(_.isOperator)
		assert_nil(_.isOp)
